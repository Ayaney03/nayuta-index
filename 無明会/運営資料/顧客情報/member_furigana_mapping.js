// 振り仮名でソートする関数
function sortByFurigana(members) {
    return members.sort((a, b) => {
        const furiganaA = memberFuriganaMapping[a.name] || a.name;
        const furiganaB = memberFuriganaMapping[b.name] || b.name;
        return furiganaA.localeCompare(furiganaB, 'ja', { numeric: true });
    });
}

// メンバーデータベースに振り仮名を追加する関数
function updateMembersWithFurigana(membersDatabase) {
    return membersDatabase.map(member => ({
        ...member,
        furigana: memberFuriganaMapping[member.name] || member.name
    }));
}

