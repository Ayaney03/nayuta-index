// メンバーデータベースに体験入会日を追加する関数
function updateMembersWithTrialDates(membersDatabase) {
    return membersDatabase.map((member, index) => {
        const trialDate = trialDatesMapping[index];
        return {
            ...member,
            trialJoinDate: trialDate || '', // 空の場合は空文字
            // 体験から正式入会までの期間を計算
            trialToPaid: trialDate && member.joinDate ? 
                Math.ceil((new Date(member.joinDate) - new Date(trialDate)) / (1000 * 60 * 60 * 24)) : null
        };
    });
}

